package de.bund.bfr.knime.pmm.secondarymodelanddataview;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

/**
 * <code>NodeFactory</code> for the "SecondaryModelAndDataView" Node.
 * 
 * 
 * @author Christian Thoens
 */
public class SecondaryModelAndDataViewNodeFactory extends
		NodeFactory<SecondaryModelAndDataViewNodeModel> {

	/**
	 * {@inheritDoc}
	 */
	@Override
	public SecondaryModelAndDataViewNodeModel createNodeModel() {
		return new SecondaryModelAndDataViewNodeModel();
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public int getNrNodeViews() {
		return 0;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeView<SecondaryModelAndDataViewNodeModel> createNodeView(
			final int viewIndex,
			final SecondaryModelAndDataViewNodeModel nodeModel) {
		return new SecondaryModelAndDataViewNodeView(nodeModel);
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public boolean hasDialog() {
		return true;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeDialogPane createNodeDialogPane() {
		return new SecondaryModelAndDataViewNodeDialog();
	}

}
